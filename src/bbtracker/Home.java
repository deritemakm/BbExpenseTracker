     private void updateLabelFromFile() {
        String username = Login.jTextField1.getText();
        String fileName = username + "_Budget.txt";
        File file = new File(fileName);
        
        try {
            BufferedReader reader = new BufferedReader(new FileReader(file));
            String budgetFromFile = reader.readLine();
            reader.close();
            
            if (budgetFromFile != null) {
                String[] parts = budgetFromFile.split(": ");
                if (parts.length == 2) {
                    String budgetOnly = parts[1];
                    jLabel5.setText(budgetOnly);
                } else {
                    throw new IOException("Unexpected file format.");
                }
            }
        } catch (IOException e) {
        }
    }
     
    private void displayTotalExpense(){
        String username = Login.jTextField1.getText();
        String fileName = username + "_expenses.txt";
        String lastestTotalExpense = "0.00"; 

        File file = new File(fileName);
    
        if (!file.exists()) {
            jLabel8.setText("0.00");
            return;
        }

        try (BufferedReader reader = new BufferedReader(new FileReader(fileName))) {
            String line;

            while ((line = reader.readLine()) != null) {
                if (line.startsWith("Total for days")) {
                    // Extract the amount from the line
                    String[] parts = line.split(":");
                    if (parts.length == 2) {
                        lastestTotalExpense = parts[1].trim(); 
                    }
                }
            }
     
        jLabel8.setText(lastestTotalExpense);
        } catch (IOException e) {
           JOptionPane.showMessageDialog(null, "Failed to load.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
      
     private void updateSave() {    
        String budgetText = jLabel5.getText().trim();
        String expenseText = jLabel8.getText().trim();

    
        if (!budgetText.isEmpty() && !expenseText.isEmpty()) {
            try {
                double currentBudget = Double.parseDouble(budgetText);
                double currentExpense = Double.parseDouble(expenseText);
                double currentSave = currentBudget - currentExpense;
                jLabel9.setText(Double.toString(currentSave));
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Failed to load.", "Error", JOptionPane.ERROR_MESSAGE);
                jLabel9.setText("Error");  
            }
        } else {
            jLabel9.setText("0.00");
        }
    }

    
