    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        double expenseAmount;
        String expenseCategory = (String) jComboBox1.getSelectedItem();
        String username = Login.jTextField1.getText();

       
        try {
            expenseAmount = Double.parseDouble(jTextField1.getText());
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Invalid amount format.");
            return;
        }

      
        Date selectedDate = jDateChooser1.getDate();
        if (selectedDate == null) {
            JOptionPane.showMessageDialog(null, "Please select a date.");
            return; 
        }

       
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String formattedDate = dateFormat.format(selectedDate);

        
        String fileName = username + "_expenses.txt";
        File expensesFile = new File(fileName);

       
        Map<String, Map<String, List<Double>>> dateToCategoryExpensesMap = new LinkedHashMap<>();

       
        if (expensesFile.exists()) {
            try (BufferedReader reader = new BufferedReader(new FileReader(fileName))) {
                String line;
                String currentDate = null;
                String currentCategory = null;

                while ((line = reader.readLine()) != null) {
                    if (line.startsWith("Transaction:")) {
                        continue; 
                    } else if (line.equals("--------------------------------------------------")) {
                        continue;  
                    } else if (line.trim().matches("\\d{4}-\\d{2}-\\d{2}")) {
                        currentDate = line.trim();
                        dateToCategoryExpensesMap.putIfAbsent(currentDate, new LinkedHashMap<>()); 
                    } else if (line.trim().matches("\\d+(\\.\\d+)?, .+")) {
                        String[] parts = line.split(", ");
                        if (parts.length == 2 && currentDate != null) {
                            double currentExpense = Double.parseDouble(parts[0]);
                            currentCategory = parts[1];
                            dateToCategoryExpensesMap.get(currentDate).putIfAbsent(currentCategory, new ArrayList<>()); 
                            dateToCategoryExpensesMap.get(currentDate).get(currentCategory).add(currentExpense); 
                        }
                    }
                }
            } catch (IOException e) {
                JOptionPane.showMessageDialog(null, "Error reading file: " + e.getMessage());
                return; 
            }
        }
        
        dateToCategoryExpensesMap.putIfAbsent(formattedDate, new LinkedHashMap<>());
        dateToCategoryExpensesMap.get(formattedDate).putIfAbsent(expenseCategory, new ArrayList<>());
        dateToCategoryExpensesMap.get(formattedDate).get(expenseCategory).add(expenseAmount);
        
        StringBuilder fileContent = new StringBuilder();
        int transactionNumber = 1;

        
        for (Map.Entry<String, Map<String, List<Double>>> dateEntry : dateToCategoryExpensesMap.entrySet()) {
            String date = dateEntry.getKey();
            Map<String, List<Double>> categoryToExpensesMap = dateEntry.getValue();

            fileContent.append("Transaction: ").append(transactionNumber++).append(System.lineSeparator());
            fileContent.append(date).append(System.lineSeparator());

            for (Map.Entry<String, List<Double>> categoryEntry : categoryToExpensesMap.entrySet()) {
                String currentCategory = categoryEntry.getKey();
                List<Double> expenses = categoryEntry.getValue();

                for (Double expense : expenses) {
                    fileContent.append(expense).append(", ").append(currentCategory).append(System.lineSeparator());
                }
            }

            fileContent.append("--------------------------------------------------").append(System.lineSeparator());
        }

        List<String> uniqueDates = new ArrayList<>(dateToCategoryExpensesMap.keySet());
        for (int i = 0; i < uniqueDates.size(); i += 7) {
            if (i + 7 <= uniqueDates.size()) {
                double periodTotal = 0.0;
                for (int j = i; j < i + 7; j++) {
                    String date = uniqueDates.get(j);
                    for (Map.Entry<String, List<Double>> categoryEntry : dateToCategoryExpensesMap.get(date).entrySet()) {
                        List<Double> expenses = categoryEntry.getValue();
                        for (Double expense : expenses) {
                            periodTotal += expense;
                        }
                    }
                }
                fileContent.append("Total for days ").append(uniqueDates.get(i)).append(" to ").append(uniqueDates.get(i + 6)).append(": ").append(periodTotal).append(System.lineSeparator());
                fileContent.append("--------------------------------------------------").append(System.lineSeparator());
            }
        }

        try (BufferedWriter writer = new BufferedWriter(new FileWriter(fileName))) {
            writer.write(fileContent.toString());
            JOptionPane.showMessageDialog(null, "Expense added successfully.");
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "Error writing to file: " + e.getMessage());
        }

    }//GEN-LAST:event_jButton2ActionPerformed
 

    
 
