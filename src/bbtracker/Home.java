    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
        String username = Login.jTextField1.getText();
        String fileName = username + "_Budget.txt";
        File file = new File(fileName);
  
        String weeklyBudgetStr = "0";
        weeklyBudgetStr = JOptionPane.showInputDialog(null, "Please enter your weekly budget:", "Weekly Budget Input", JOptionPane.QUESTION_MESSAGE);

        if (weeklyBudgetStr != null && !weeklyBudgetStr.isEmpty()) {
            try {
                double weeklyBudget = Double.parseDouble(weeklyBudgetStr);
                FileWriter writer = new FileWriter(file);
                writer.write("Weekly Budget: " + weeklyBudget);
                writer.close();

                updateLabelFromFile();
                displayTotalExpense();
                updateSave();

                JOptionPane.showMessageDialog(null, "Weekly budget saved successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);

            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Invalid input. Please enter a valid number for the budget.", "Error", JOptionPane.ERROR_MESSAGE);
            } catch (IOException e) {
                JOptionPane.showMessageDialog(null, "An error occurred while saving the budget.","Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
           JOptionPane.showMessageDialog(null, "Budget input was cancelled or left empty.", "No Input", JOptionPane.WARNING_MESSAGE);
        }
    
    }//GEN-LAST:event_jButton1ActionPerformed

