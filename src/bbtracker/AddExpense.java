    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed

        int option = JOptionPane.showConfirmDialog(this,"Are you sure you want to delete all your expenses?","Confirmation", JOptionPane.YES_NO_OPTION);

        if (option == JOptionPane.YES_OPTION) {
            String username = Login.jTextField1.getText();
            String fileName = username + "_expenses.txt";

        File deleteExpenses = new File(fileName);

            if (deleteExpenses.exists()) {
                boolean deleted = deleteExpenses.delete();
                if (deleted) {
                    JOptionPane.showMessageDialog(this,"All expenses deleted successfully.", "File Deleted", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this, "Failed to delete your expenses.","Error", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this,"No expenses.","Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton6ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        String username = Login.jTextField1.getText();
        String fileName = username + "_expenses.txt";

        List<TransactionList.Transaction> transactions = new ArrayList<>();
        TransactionList.Transaction currentTransaction = null;

        try (BufferedReader br = new BufferedReader(new FileReader(fileName))) {
            String line;
            while ((line = br.readLine()) != null) {
                if (line.startsWith("Transaction:")) {
                    if (currentTransaction != null) {
                        transactions.add(currentTransaction);
                    }
                    String date = br.readLine().trim();
                    currentTransaction = new TransactionList.Transaction(date);
                } else if (line.matches("^\\d+\\.\\d+, .+$")) {
                    String[] parts = line.split(", ");
                    try {
                        double amount = Double.parseDouble(parts[0]);
                        String description = parts[1];
                        currentTransaction.addTransaction(amount, description);
                    } catch (NumberFormatException | ArrayIndexOutOfBoundsException e) {
                        JOptionPane.showMessageDialog(null, "Error", "Error", JOptionPane.ERROR_MESSAGE);
                    }
                }
            }
            
            if (currentTransaction != null) {
                transactions.add(currentTransaction);
            }
        } catch (IOException e) {
             JOptionPane.showMessageDialog(null, "No transactions found.", "Message", JOptionPane.INFORMATION_MESSAGE);
        }

        SwingUtilities.invokeLater(() -> new TransactionList.TransactionListGUI(transactions));

        this.dispose();
    }//GEN-LAST:event_jButton5ActionPerformed

